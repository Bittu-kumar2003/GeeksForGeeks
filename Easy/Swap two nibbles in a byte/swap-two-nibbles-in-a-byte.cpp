class Solution {
  public:
    int swapNibbles(int n) {
       string s = bitset<8>(n).to_string();
        for(int i=0;i<4;i++) swap(s[i],s[i+4]);
        return stoi(s,0,2);
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        cout << ob.swapNibbles(n) << endl;
    }
    return 0;
}
